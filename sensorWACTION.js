    bmak["mme_cnt"]=max_number
    bmak["mduce_cnt"]=1
    bmak["me_cnt"]= max_number +  1
    initpoint = 0;
    data = []
    cnt = 0
    //Set initial point
    while(initpoint<=0) {
        initpoint = Math.floor(Math.random(0,1) * mmp.length - max_number )
    }
    //Generate mouse movement
    while(cnt<max_number) {
        if (cnt == 0) {
            data[cnt] = `${cnt},`+"1," + base_time + `,${mmp[initpoint].x},${mmp[initpoint].y}`
        } else {
            action_time = Math.floor(Math.random(0,1) * 5 +7)
            base_time += action_time
            data[cnt] = `${cnt},`+"1," + base_time + `,${mmp[initpoint + cnt].x},${mmp[initpoint+cnt].y}`
        }
        bmak["me_vel"] = bmak["me_vel"] + cnt + 1 + base_time + mmp[initpoint + cnt].x + mmp[initpoint+cnt].y    
        bmak["ta"] += i
        cnt++
    }
    //Generate mouse down
    action_time = Math.floor(Math.random(0,1) * 5 +7)
            base_time += action_time
    data[cnt] = `${cnt},`+"3," + base_time + `,${mmp[initpoint + cnt-1].x},${mmp[initpoint+cnt-1].y}` + ",-1"
